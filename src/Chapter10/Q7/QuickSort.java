public class QuickSort implements Sort {

    @Override
    public int[] ascending(int[] arr) {
        System.out.println("Ascending Array Via Quick Sort");
        return arr;
    }

    @Override
    public int[] descending(int[] arr) {
        System.out.println("Descending Array Via Quick Sort");
        return arr;
    }
    
}
